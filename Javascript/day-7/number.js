// these are not case sensetive
let n1 = 234;
console.log(n1); //234

//octal
let no = 0o234;
let no1 = 0o234;
console.log(no); //156   // octal to decimal
console.log(no1); //156   // octal to decimal

//binary
let nb = 0b110;
let nb1 = 0b110;
console.log(nb); //6    binary to decimal
console.log(nb1); //6    binary to decimal

//hexadecimal
let nx = 0x2a4;
let nx1 = 0x2a4;
console.log(nx); //676 hex to decimal
console.log(nx1); //676 hex to decimal

//exponential numbers

let exp = 1e4;
let exp1 = 1e4;
console.log(exp); //10000
console.log(exp1); //10000

