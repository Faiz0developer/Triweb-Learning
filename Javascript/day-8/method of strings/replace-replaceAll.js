//  replace(a.b)   ->  a arguement is replace by b arguement
const str = 'It is animal';

const str1 = str.replace('animal','bird')
console.log(str1)     //It is bird



//  replaceAll()
const str2 = 'It is animal and its a big animal';

const str3 = str2.replaceAll('animal','bird')
console.log(str3)    //It is bird and its a big bird