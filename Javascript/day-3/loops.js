// while loop
let sn2 = 1;
while (sn2 <= 10) {
  console.log(sn2 + " While loop");
  sn2++;
}

// do-while loop
let sn3 = 1;
do {
  console.log(sn3 + " do-while loop");
  sn3++;
} while (sn3 <= 10);
